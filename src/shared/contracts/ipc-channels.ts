export const ipcChannels = Object.fromEntries(Object.entries(ipcChannelMetadata).map(([key, metadata]) => [key, metadata.channel])) as {
  readonly [Key in keyof typeof ipcChannelMetadata]: (typeof ipcChannelMetadata)[Key]["channel"];
};

export type IpcChannelKey = keyof typeof ipcChannelMetadata;
export type IpcChannel = (typeof ipcChannels)[IpcChannelKey];
export type IpcInvokeChannelKey = {
  [Key in IpcChannelKey]: (typeof ipcChannelMetadata)[Key]["kind"] extends "invoke" ? Key : never;
}[IpcChannelKey];
export type IpcSendChannelKey = {
  [Key in IpcChannelKey]: (typeof ipcChannelMetadata)[Key]["kind"] extends "send" ? Key : never;
}[IpcChannelKey];
export type IpcEventChannelKey = {
  [Key in IpcChannelKey]: (typeof ipcChannelMetadata)[Key]["kind"] extends "event" ? Key : never;
}[IpcChannelKey];

export const ipcInvokeChannelKeys = Object.keys(ipcChannelMetadata).filter(
  (key): key is IpcInvokeChannelKey => ipcChannelMetadata[key as IpcChannelKey].kind === "invoke"
);

export const ipcRendererOnlyChannelKeys = Object.keys(ipcChannelMetadata).filter(
  (key): key is Exclude<IpcChannelKey, IpcInvokeChannelKey> => ipcChannelMetadata[key as IpcChannelKey].kind !== "invoke"
);

export const ipcAuditCategories = {
  localFilesystemReads: channelsForRisk("local_filesystem_read"),
  localGitMutations: channelsForRisk("local_git_mutation", "destructive_local_git_mutation"),
  destructiveMutations: channelsForRisk("destructive_local_git_mutation"),
  networkWrites: channelsForRisk("network_write"),
  externalAppLaunches: channelsForRisk("external_app_launch")
} as const;

export const ipcAuditReport = {
  localFilesystemReads: auditEntries(ipcAuditCategories.localFilesystemReads),
  localGitMutations: auditEntries(ipcAuditCategories.localGitMutations),
  destructiveMutations: auditEntries(ipcAuditCategories.destructiveMutations),
  networkWrites: auditEntries(ipcAuditCategories.networkWrites),
  externalAppLaunches: auditEntries(ipcAuditCategories.externalAppLaunches)
} as const satisfies {
  readonly [Category in keyof typeof ipcAuditCategories]: ReadonlyArray<{
    readonly key: (typeof ipcAuditCategories)[Category][number];
    readonly channel: (typeof ipcChannelMetadata)[(typeof ipcAuditCategories)[Category][number]]["channel"];
    readonly group: IpcChannelGroup;
    readonly risk: IpcChannelRisk;
    readonly repoVisibility: IpcRepoVisibilityPolicy;
    readonly operationRecord: IpcOperationRecordPolicy;
    readonly confirmationPolicy: IpcConfirmationPolicy;
  }>;
};

function auditEntries<Keys extends readonly IpcChannelKey[]>(keys: Keys) {
  return keys.map((key) => ({
    key,
    channel: ipcChannelMetadata[key].channel,
    group: ipcChannelMetadata[key].group,
    risk: ipcChannelMetadata[key].risk,
    repoVisibility: ipcChannelMetadata[key].repoVisibility,
    operationRecord: ipcChannelMetadata[key].operationRecord,
    confirmationPolicy: ipcChannelMetadata[key].confirmationPolicy
  }));
}

function channelsForRisk(...risks: IpcChannelRisk[]): IpcChannelKey[] {
  const allowed = new Set<IpcChannelRisk>(risks);
  return Object.keys(ipcChannelMetadata).filter((key): key is IpcChannelKey => allowed.has(ipcChannelMetadata[key as IpcChannelKey].risk));
}
